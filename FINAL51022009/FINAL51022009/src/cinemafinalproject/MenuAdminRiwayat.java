    private void hapusRiwayat(String idPembelian) {
        try (Database db = new Database("DELETE FROM pembelian WHERE id_tiket = ?")) {
            db.setString(1, idPembelian);
            int rowsAffected = db.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan atau gagal dihapus", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
}
    
