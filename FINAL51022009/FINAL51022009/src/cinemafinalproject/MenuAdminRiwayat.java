    private void loadDataToTable() {
        DefaultTableModel model = (DefaultTableModel) tabelriwayat.getModel();
        model.setRowCount(0); // Bersihkan semua baris yang ada sebelum memuat data baru

        try (Database db = new Database("SELECT * FROM userfilm")) {
            var rs = db.executeQuery();
            while (rs.next()) {
            String id_tiket = rs.getString("id_tiket");
            String id_user = rs.getString("id_user");
            String id_film = rs.getString("id_film");
            int jumlah_tiket = rs.getInt("jumlah_tiket");
            String no_kursi = rs.getString("no_kursi");
            String total_harga_tiket = rs.getString("total_harga_tiket");
            String total_bayar = rs.getString("total_bayar");
            String metode_pembayaran = rs.getString("metode_pembayaran");
            String waktu_pemesanan = rs.getString("waktu_pemesanan");
            model.addRow(new Object[]{id_tiket, id_user, id_film, jumlah_tiket, no_kursi, total_harga_tiket, total_bayar, metode_pembayaran, waktu_pemesanan});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
}
    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        int baris = tabelriwayat.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(null, "Pilih baris yang ingin dihapus", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String idTiket = tabelriwayat.getValueAt(baris, 0).toString();

        try {
            hapusRiwayat(idTiket);
            DefaultTableModel model = (DefaultTableModel) tabelriwayat.getModel();
            model.removeRow(baris);

            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_hapusActionPerformed

