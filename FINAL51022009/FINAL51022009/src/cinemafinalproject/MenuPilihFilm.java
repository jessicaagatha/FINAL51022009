    private void loadDataToTable() {
        DefaultTableModel model = (DefaultTableModel) tabelpenayanganuser.getModel();
        model.setRowCount(0); // Bersihkan semua baris yang ada sebelum memuat data baru

        try (Database db = new Database("SELECT * FROM film")) {
            ResultSet rs = db.executeQuery();
            while (rs.next()) {
                String idFilm = rs.getString("id_film");
                String namaFilm = rs.getString("judul");
                String genreFilm = rs.getString("genre");
                String producerFilm = rs.getString("producer");
                String sutradaraFilm = rs.getString("sutradara");
                String penulisFilm = rs.getString("penulis");
                String pemeranFilm = rs.getString("pemeran");
                String durasiFilm = rs.getString("durasi_film");
                String hariTayangFilm = rs.getString("hari_tayang");
                String waktuTayangFilm = rs.getString("waktu_tayang");
                String statusFilm = rs.getString("status_tayang");
                String sinopsisFilm = rs.getString("sinopsis_film");
                byte[] gambarFilm = rs.getBytes("gambar_film");
                ImageIcon icon = new ImageIcon(gambarFilm);
                Image image = icon.getImage().getScaledInstance(120, 120, Image.SCALE_SMOOTH);
                ImageIcon resizedIcon = new ImageIcon(image);
                model.addRow(new Object[]{idFilm, namaFilm, genreFilm, producerFilm, sutradaraFilm, penulisFilm, pemeranFilm, durasiFilm, hariTayangFilm, 
                    waktuTayangFilm, statusFilm, sinopsisFilm, resizedIcon});
            }

            tabelpenayanganuser.getColumnModel().getColumn(12).setCellRenderer(new MenuPilihFilm.ImageRenderer());
            setTableRenderer(tabelpenayanganuser, 120, 120); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void setTableRenderer(JTable table, int width, int height) {
        for (int i = 0; i < table.getColumnCount(); i++) {
            table.getColumnModel().getColumn(i).setPreferredWidth(width);
        }
        table.setRowHeight(height);
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);
        table.setDefaultRenderer(Object.class, centerRenderer); 
    }
    
    class ImageRenderer extends DefaultTableCellRenderer {
        @Override
        protected void setValue(Object value) {
            if (value instanceof ImageIcon) {
                setIcon((ImageIcon) value);
            } else {
                super.setValue(value);
            }
        }
    }
    
    private void pesanKursi(JButton kursiButton) {
        String idFilm = idfilm.getText();
        String noKursi = kursiButton.getText().trim();

        if (!cekKursiTersedia(idFilm, noKursi)) {
            JOptionPane.showMessageDialog(this, "Maaf, kursi sudah terpesan.", "Kursi Tidak Tersedia", JOptionPane.WARNING_MESSAGE);
            return; 
        }

        pilihkursi.append(noKursi + " , ");
        kursiButton.setBackground(Color.BLUE);
        kursiButton.setEnabled(false); 
}
    
    private boolean cekKursiTersedia(String idFilm, String noKursi) {
        try {
            String query = "SELECT COUNT(*) FROM userfilm WHERE id_film = ? AND no_kursi = ?";
            Database db = new Database(query);
            db.setString(1, idFilm);
            db.setString(2, noKursi);
            ResultSet rs = db.executeQuery();

            if (rs.next()) {
                int count = rs.getInt(1);
                return count == 0; 
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat memeriksa ketersediaan kursi.", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
        return false; 
    }
    
