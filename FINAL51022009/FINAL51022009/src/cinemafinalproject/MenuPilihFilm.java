    private List<String> getKursiDipesan(String idFilm) {
        List<String> kursiDipesan = new ArrayList<>();

        try {
            String query = "SELECT no_kursi FROM userfilm WHERE id_film = ?";
            Database db = new Database(query);
            db.setString(1, idFilm);
            ResultSet rs = db.executeQuery();
            while (rs.next()) {
                String noKursi = rs.getString("no_kursi");
                if (noKursi != null) {
                    noKursi = noKursi.trim();
                    kursiDipesan.add(noKursi);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengambil data kursi yang dipesan.", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

        return kursiDipesan;
}

    private void tandaiKursiDipesan(List<String> kursiDipesan) {
        for (String kursi : kursiDipesan) {
            JButton kursiButton = carikursi(kursi);
            if (kursiButton != null) {
                kursiButton.setBackground(Color.RED);
                kursiButton.setEnabled(false);
            }
        }

    }
    private JButton carikursi(String nokursi) {
        Component[] c = kursiPanel.getComponents();

        for (Component component : c) {
            if (component instanceof JButton) {
                JButton button = (JButton) component;
                if (button.getText().equals(nokursi)) {
                    return button;
                }
            }
        }

        return null;
    }
    
